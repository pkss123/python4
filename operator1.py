# 컴퓨터는 애초에 계산기로 만들어졌기 때문에 계산기능을
# 가지고 있습니다. 따라서 우리가 알고있는 사칙연산인
# +, -, *, / 등의 연산을 수행할 수 있습니다.

print(10+4, 10-4, 10*4, 10/4)

# / 나누기는 결과를 실수로 얻어오기 때문에
# // 을 이용해서 정수 나눗셈을 사용할 수도 있다

print(10//4)

# * 는 곱하기 이지만
# **은 제곱을 의미합니다. a ** b 는 a의 b제곱을 의미함

print(10 ** 4)

# % 는 나눗셈의 정수 몫을 구하고 남은 나머지결과를 얻습니다
# 나머지 연산자는 정수를 이용한 나눗셈에서만 사용할 수 있습니다
print(10 % 7)
print( 841 % 27)